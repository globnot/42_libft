#include "libft.h"

char	*ft_strdup(char *src)
{
	size_t	len;
	char	*dest;
	int		i;

	len = ft_strlen(src);
	dest = malloc(sizeof(char *) * len + 1);
	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
