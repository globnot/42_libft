#include "libft.h"

void	ft_putnbr(int nb)
{
	if (nb == FT_INT_MIN)
	{
		ft_putstr("-2147483648");
		return ;
	}
	else if (nb < 0)
	{
		nb = nb * -1;
		ft_putchar('-');
	}
	if (nb >= 10)
		ft_putnbr(nb / 10);
	ft_putchar((nb % 10) + '0');
}
